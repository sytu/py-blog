# 获取注册用户数据的 JSON格式的REST API
@get('/api/users')
async def api_get_users():
    users = await User.findAll(orderBy='created_at desc')
    for u in users:
        u.password = '******'
    return dict(users=users) # 只要返回一个dict，后续的response这个middleware就可以把结果序列化为JSON并返回




























































